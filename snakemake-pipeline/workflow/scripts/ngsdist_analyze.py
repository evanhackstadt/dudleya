# Analyze genetic distance matrix output from ngsDist
#
# Distance matrix format (ngsDist output):
#   Line 1: empty
#   Line 2: number of samples (integer)
#   Lines 3+: <sample_label>\t<d1>\t<d2>\t...\t<dN>
#
# Species inference logic:
#   CY_*        -> DUCY (D. cymosa)
#   ABMU/ABBE/ABAB_* -> DUAB (D. abramsii)
#   all others  -> DUSE (D. setchellii)
#
# Analyses:
#   1. Mean pairwise between-species distances (DUSE-DUCY, DUSE-DUAB, DUCY-DUAB)
#      with permutation-based significance tests (accounts for non-independence)
#   2. Focal putative hybrid CY_HUN_LP_267_Du-686:
#      mean distance to each species + permutation test for species affinity
